/* Types of Binary Trees

1. Full Binary Tree - either have 0 child or 2 children.
2. Complete Binary Tree - All levels are completely filled except the last level. The last level has all nodes in left.
3. Perfect Binary Tree - All leaf nodes are at same level.
4. Balanced Binary TRees - Height of the tree should be log(N) where N = number of nodes.
5. Degenerete Trees - Linear trees like a straight line which looks like a linked list.
*/